package com.yaowan.csv.entity;

public class CoinShopCsv {
	
	private int itemOrder;
	private String itemName;
	private String itemPrice;
	private int golddquantity;
	private String itemInfo;
	private String bonus;
	private int bonusItem;
	private String itemIcon;
	
	public int getItemOrder() {
		return itemOrder;
	}
	public void setItemOrder(int itemOrder) {
		this.itemOrder = itemOrder;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemPrice() {
		return itemPrice;
	}
	public void setItemPrice(String itemPrice) {
		this.itemPrice = itemPrice;
	}
	public int getGolddquantity() {
		return golddquantity;
	}
	public void setGolddquantity(int golddquantity) {
		this.golddquantity = golddquantity;
	}
	public String getItemInfo() {
		return itemInfo;
	}
	public void setItemInfo(String itemInfo) {
		this.itemInfo = itemInfo;
	}
	public String getBonus() {
		return bonus;
	}
	public void setBonus(String bonus) {
		this.bonus = bonus;
	}
	public int getBonusItem() {
		return bonusItem;
	}
	public void setBonusItem(int bonusItem) {
		this.bonusItem = bonusItem;
	}
	public String getItemIcon() {
		return itemIcon;
	}
	public void setItemIcon(String itemIcon) {
		this.itemIcon = itemIcon;
	}
	
}
