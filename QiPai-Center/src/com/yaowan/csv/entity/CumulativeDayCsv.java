package com.yaowan.csv.entity;

public class CumulativeDayCsv {

	private int Id;
	private int CumulativeDay;
	private String RewardItem;

	
	public int getId() {
		return Id;
	}
	public void setId(int Id) {
		this.Id = Id;
	}
	
	public int getCumulativeDay() {
		return CumulativeDay;
	}
	public void setCumulativeDay(int CumulativeDay) {
		this.CumulativeDay = CumulativeDay;
	}
	public String getRewardItem() {
		return RewardItem;
	}
	public void setRewardItem(String RewardItem) {
		this.RewardItem = RewardItem;
	}
	

	
}
